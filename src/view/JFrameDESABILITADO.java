    //Executava o Cadastro de Usuário ou Cliente
    private void jBtCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtCadastrarActionPerformed

        if(UsuOuClt == 1){
            //PODE SER REAPROVEITADO
            Usuario usuario = new Usuario();
            usuario.setNome(jTxtNome.getText());
            usuario.setCpf(jTxtCpf.getText());
            usuario.setEmail(jTxtEmail.getText());
            usuario.setLogin(jTxtLogin.getText());
            usuario.setSenha(jPassSenha.getText());
            usuario.setCargo(String.valueOf(jCbCargo.getSelectedItem()));
        
            UsuarioDAO usuarioDAO = new UsuarioDAO();
        
            usuarioDAO.salvar(usuario);
            this.dispose();
        } else{
            //PODE SER REAPROVEITADO
            Cliente cliente = new Cliente();
            cliente.setNome(jTxtNome.getText());
            cliente.setCpf(jTxtCpf.getText());
            cliente.setEndereco(jTxtEndereco.getText());
            cliente.setData_nasc(jTxtDataNasc.getText());
            cliente.setEmail(jTxtEmail.getText());
            cliente.setLogin(jTxtLogin.getText());
            cliente.setSenha(jPassSenha.getText());
            
            Usuario usuario = new Usuario();
            usuario.setId_usuario(Integer.parseInt(jLabelId.getText()));
            
            ClienteDAO clienteDAO = new ClienteDAO();
            clienteDAO.salvar(cliente,usuario);
            this.dispose();
        }
        
        JOptionPane.showMessageDialog(this, "Cadastro realizado!");
        this.dispose();
    }//GEN-LAST:event_jBtCadastrarActionPerformed


    //Habilitava ou desabilitava componentes caso o resultado incrementado fosse true(Usuário) ou false(Cliente)
    public void opLogin(boolean op){
        if(op == true){
            jCbCargo.setVisible(true);
            jPanelContato.setVisible(false);
            jTxtEndereco.setVisible(false);
            UsuOuClt = 1; //Administrador
        } else{
            jCbCargo.setVisible(false);
            jLabelCargo.setVisible(false);
            UsuOuClt = 2;//Cliente
        }
    }
    
    //Incluia os cargos Administrador e Bibliotecário no ComboCox de Cargos
